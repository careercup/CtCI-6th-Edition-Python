def position_is_valid(row, col, board):

    # Check if there are any queens in the rows above this position
    for cur_row in range(row):
        if board[cur_row][col] == "Q":
            return False

    # Check if there are any queens in the cols along this position
    # This is a sanity check since our main function shouldn't add more than
    # one queen at any row
    for cur_col in range(len(board[row])):
        if board[row][cur_col] == "Q":
            return False

    # Check if there is a queen in the upper left diagonal
    cur_row = row
    cur_col = col
    while cur_row >= 0 and cur_col >= 0:
        if board[cur_row][cur_col] == "Q":
            return False
        cur_row -= 1
        cur_col -= 1

    # Check if there is a queen in the upper right diagonal
    cur_row = row
    cur_col = col
    while cur_row >= 0 and cur_col < len(board[row]):
        if board[cur_row][cur_col] == "Q":
            return False
        cur_row -= 1
        cur_col += 1
    return True


def queens(n=8):
    def helper(row, board):
        if row == n:
            arrangements.append(copy.deepcopy(board))
            # print('$'*20)
            # pprint.pprint(board)
            # print('$'*20)
        else:
            for col in range(len(board[row])):
                if position_is_valid(row, col, board):
                    board[row][col] = "Q"
                    helper(row + 1, copy.deepcopy(board))
                    board[row][col] = "."
                    # pprint.pprint(board)

    arrangements = []
    helper(0, [["." for _ in range(8)] for _ in range(8)])
    return arrangements


class TestSuite(unittest.TestCase):
    def setUp(self):
        self.board = [["." for _ in range(8)] for _ in range(8)]

    def test_empty_board_for_validity(self):
        assert position_is_valid(0, 0, self.board)
        assert position_is_valid(7, 7, self.board)
        assert position_is_valid(3, 5, self.board)

    def test_invalid_row_for_validity(self):
        self.board[4][3] = "Q"
        assert not position_is_valid(4, 2, self.board)
        self.board[4][3] = "."

    def test_invalid_col_for_validity(self):
        self.board[4][3] = "Q"
        assert not position_is_valid(7, 3, self.board)
        self.board[4][3] = "."

    def test_invalid_upper_left_diag_for_validity(self):
        self.board[0][0] = "Q"
        assert not position_is_valid(5, 5, self.board)
        self.board[0][0] = "."
        self.board[1][2] = "Q"
        assert not position_is_valid(4, 5, self.board)
        self.board[1][2] = "."

    def test_invalid_upper_right_diag_for_validity(self):
        self.board[2][7] = "Q"
        assert not position_is_valid(3, 6, self.board)
        self.board[2][7] = "."

